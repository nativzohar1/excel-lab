function initApp() {
    const langToggleBtn = document.getElementById('lang-toggle-btn');
    const langLabel = document.getElementById('lang-label');
    const htmlEl = document.documentElement;
    
    // Logic: Default to user system preference, unless they saved a choice.
    const userLang = navigator.language.startsWith('he') ? 'he' : 'en';
    const savedLang = localStorage.getItem('site-lang');
    let currentLang = savedLang || userLang;

    // Force update if default is missing or valid
    if (!['he', 'en'].includes(currentLang)) {
        currentLang = 'he'; 
    }

    function updateContent(lang) {
        // Set HTML attributes
        htmlEl.setAttribute('lang', lang);
        htmlEl.setAttribute('dir', lang === 'he' ? 'rtl' : 'ltr');
        
        // Update Text
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                // Check if it's an input placeholder or standard text
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = translations[lang][key];
                } else {
                    el.innerText = translations[lang][key];
                }
            }
        });

        // Update Button Label (Show the OTHER language)
        if(langLabel) {
            langLabel.innerText = lang === 'he' ? 'English' : 'עברית';
        }
        
        localStorage.setItem('site-lang', lang);
    }
    
    // Initial Load
    updateContent(currentLang);
    
    // Event Listener
    if(langToggleBtn) {
        langToggleBtn.addEventListener('click', () => {
            currentLang = currentLang === 'he' ? 'en' : 'he';
            updateContent(currentLang);
        });
    }

    // Fix static/hash links to smooth scroll if they are anchors
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href').substring(1);
            const targetElement = document.getElementById(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    });

    // Carousel Logic
    const track = document.getElementById('testimonial-track');
    const prevBtn = document.getElementById('carousel-prev');
    const nextBtn = document.getElementById('carousel-next');
    const dotsContainer = document.getElementById('carousel-dots');
    
    if (track && prevBtn && nextBtn) {
        let currentIndex = 0;
        
        function getSlidesToShow() {
            return window.innerWidth >= 768 ? 2 : 1;
        }

        function updateCarousel() {
            const slidesToShow = getSlidesToShow();
            const slides = track.children;
            const totalSlides = slides.length;
            const slideWidth = 100 / slidesToShow;
            
            const isRTL = document.documentElement.dir === 'rtl';
            // In LTR: Next item is Right -> Move Left (Negative)
            // In RTL: Next item is Left -> Move Right (Positive)
            const directionMultiplier = isRTL ? 1 : -1; 
            
            const translateValue = currentIndex * slideWidth * directionMultiplier; 
            track.style.transform = `translateX(${translateValue}%)`;

            // Update disabled states
            const maxIndex = totalSlides - slidesToShow;
            
            // Safeguard index
            if(currentIndex > maxIndex) currentIndex = maxIndex;
            if(currentIndex < 0) currentIndex = 0;

            prevBtn.disabled = currentIndex <= 0;
            nextBtn.disabled = currentIndex >= maxIndex;
            prevBtn.style.opacity = currentIndex <= 0 ? '0.3' : '1';
            nextBtn.style.opacity = currentIndex >= maxIndex ? '0.3' : '1';
            
            updateDots(currentIndex, maxIndex);
        }

        function createDots() {
            if(!dotsContainer) return;
            dotsContainer.innerHTML = '';
            
            const slidesToShow = getSlidesToShow();
            const slides = track.children;
            const totalSlides = slides.length;
            const maxIndex = totalSlides - slidesToShow; 
            
            // Create dots for possible positions
            for (let i = 0; i <= maxIndex; i++) {
                const dot = document.createElement('button');
                // Basic classes
                dot.className = `w-2 md:w-3 h-2 md:h-3 rounded-full transition-colors`;
                dot.ariaLabel = `Go to slide ${i + 1}`;
                dot.addEventListener('click', () => {
                    currentIndex = i;
                    updateCarousel();
                });
                dotsContainer.appendChild(dot);
            }
            updateDots(currentIndex, maxIndex);
        }

        function updateDots(activeIdx, maxIdx) {
             if(!dotsContainer) return;
             const dots = dotsContainer.children;
             for(let i=0; i<dots.length; i++) {
                 // Reset classes
                 dot = dots[i];
                 if(i === activeIdx) {
                     dot.className = `w-3 h-3 rounded-full transition-colors bg-brand-600 scale-125`;
                 } else {
                     dot.className = `w-3 h-3 rounded-full transition-colors bg-gray-300 dark:bg-gray-600 hover:bg-gray-400`;
                 }
             }
        }

        prevBtn.addEventListener('click', () => {
            if (currentIndex > 0) {
                currentIndex--;
                updateCarousel();
            }
        });

        nextBtn.addEventListener('click', () => {
             const slidesToShow = getSlidesToShow();
             const slides = track.children;
             const totalSlides = slides.length;
            if (currentIndex < totalSlides - slidesToShow) {
                currentIndex++;
                updateCarousel();
            }
        });

        window.addEventListener('resize', () => {
            // Re-calculate limits
            const slidesToShow = getSlidesToShow();
            const slides = track.children;
            const totalSlides = slides.length;
            if (currentIndex > totalSlides - slidesToShow) {
                currentIndex = totalSlides - slidesToShow;
            }
            createDots(); // Dots amount might change if we change logic, here it changes because maxIndex changes
            updateCarousel();
        });

        // Observer for lang/dir change
        const observer = new MutationObserver(() => {
            updateCarousel();
        });
        observer.observe(document.documentElement, { attributes: true, attributeFilter: ['dir'] });

        // Init
        createDots();
        setTimeout(updateCarousel, 50);
    }
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initApp);
} else {
    initApp();
}
