document.addEventListener('DOMContentLoaded', () => {
    const langToggleBtn = document.getElementById('lang-toggle-btn');
    const langLabel = document.getElementById('lang-label');
    const htmlEl = document.documentElement;
    
    // Logic: Default to user system preference, unless they saved a choice.
    const userLang = navigator.language.startsWith('he') ? 'he' : 'en';
    const savedLang = localStorage.getItem('site-lang');
    let currentLang = savedLang || userLang;

    // Force update if default is missing or valid
    if (!['he', 'en'].includes(currentLang)) {
        currentLang = 'he'; 
    }

    function updateContent(lang) {
        // Set HTML attributes
        htmlEl.setAttribute('lang', lang);
        htmlEl.setAttribute('dir', lang === 'he' ? 'rtl' : 'ltr');
        
        // Update Text
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                // Check if it's an input placeholder or standard text
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = translations[lang][key];
                } else {
                    el.innerText = translations[lang][key];
                }
            }
        });

        // Update Button Label (Show the OTHER language)
        if(langLabel) {
            langLabel.innerText = lang === 'he' ? 'English' : 'עברית';
        }
        
        localStorage.setItem('site-lang', lang);
    }
    
    // Initial Load
    updateContent(currentLang);
    
    // Event Listener
    if(langToggleBtn) {
        langToggleBtn.addEventListener('click', () => {
            currentLang = currentLang === 'he' ? 'en' : 'he';
            updateContent(currentLang);
        });
    }

    // Fix static/hash links to smooth scroll if they are anchors
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href').substring(1);
            const targetElement = document.getElementById(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    });
});
